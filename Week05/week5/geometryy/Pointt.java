package week5.geometryy;

public class Pointt {
    int x;
    int y;
    public  Pointt(int x, int y){
        this.x = x;
        this.y = y;
    }
    public Pointt() {
    }
    public void setX(int x) {
        this.x = x;
        
    }
    public void setY(int y) {
        this.y = y;
    }
     
    
}
